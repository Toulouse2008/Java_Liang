/** 2.18 Print a table. Cast floating point numbers into integers */

public class printTable{
    public static void main(String[] args){
    
        System.out.println("a\tb\tpow(a, b)\n");
        System.out.println("1\t2\t" + (int)Math.pow(1, 2) + "\n");
        System.out.println("2\t3\t" + (int)Math.pow(2, 3) + "\n");
        System.out.println("3\t4\t" + (int)Math.pow(3, 4) + "\n");
        System.out.println("4\t5\t" + (int)Math.pow(4, 5) + "\n");
        System.out.println("5\t6\t" + (int)Math.pow(5, 6) + "\n");

     }
}
